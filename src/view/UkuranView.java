    // inner class
    public class UkuranTableModel extends AbstractTableModel {

        private List<Ukuran> listUkuran = new ArrayList<>();

        private final String HEADER[] = {"Ukuran Pakaian"};

        public UkuranTableModel(List<Ukuran> listUkuran) {
            this.listUkuran = listUkuran;
        }

        @Override
        public int getRowCount() { // jumlah baris
            return listUkuran.size();
        }

        @Override
        public int getColumnCount() { // jumlah kolom
            return HEADER.length;
        }

        @Override
        public String getColumnName(int i) { // nama kolom
            return HEADER[i];
        }

        @Override
        public Object getValueAt(int i, int i1) { // mengisi data
            Ukuran s = listUkuran.get(i); // baris
            switch (i1) { // kolom
                case 0:
                    return s.getUkuran();
                default:
                    return null;
            }
        }
    }
